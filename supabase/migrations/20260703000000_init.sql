-- Step 1: Enable the pgvector extension for semantic similarity search
CREATE EXTENSION IF NOT EXISTS vector;

-- Step 1: Create the 'submissions' table for raw citizen submissions
CREATE TABLE IF NOT EXISTS submissions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    timestamp TIMESTAMPTZ DEFAULT now() NOT NULL,
    channel TEXT DEFAULT 'web' NOT NULL,
    raw_text TEXT,
    audio_url TEXT,
    photo_url TEXT,
    language TEXT NOT NULL,
    geo JSONB,
    citizen_id_hash TEXT NOT NULL
);

-- Add comments for 'submissions' mapping to roadmap schema
COMMENT ON TABLE submissions IS 'Stores raw citizen submissions containing text, voice (audio_url), or photo (photo_url) inputs.';
COMMENT ON COLUMN submissions.id IS 'Unique identifier for the submission';
COMMENT ON COLUMN submissions.timestamp IS 'Time when the submission was made';
COMMENT ON COLUMN submissions.channel IS 'Intake channel (e.g. web, whatsapp)';
COMMENT ON COLUMN submissions.raw_text IS 'Original user-provided text, if any';
COMMENT ON COLUMN submissions.audio_url IS 'URL of recorded audio/voice note, if any';
COMMENT ON COLUMN submissions.photo_url IS 'URL of uploaded photo, if any';
COMMENT ON COLUMN submissions.language IS 'Detected language of submission';
COMMENT ON COLUMN submissions.geo IS 'Geographic coordinates or administrative ward information (JSON format)';
COMMENT ON COLUMN submissions.citizen_id_hash IS 'Hashed identifier for the citizen to preserve anonymity';

-- Step 1: Create the 'enriched_submissions' table for AI enrichment results
CREATE TABLE IF NOT EXISTS enriched_submissions (
    id UUID PRIMARY KEY REFERENCES submissions(id) ON DELETE CASCADE,
    normalized_text_en TEXT NOT NULL,
    category TEXT NOT NULL,
    need_type TEXT NOT NULL,
    urgency TEXT NOT NULL,
    sentiment TEXT NOT NULL,
    canonical_location TEXT,
    extracted_entities JSONB DEFAULT '{}'::jsonb NOT NULL,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL
);

-- Add comments for 'enriched_submissions' mapping to roadmap schema
COMMENT ON TABLE enriched_submissions IS 'Stores AI-enriched submission metadata generated by the enrichment pipeline.';
COMMENT ON COLUMN enriched_submissions.id IS 'Foreign key referencing the raw submission';
COMMENT ON COLUMN enriched_submissions.normalized_text_en IS 'Normalized and translated English version of the raw submission';
COMMENT ON COLUMN enriched_submissions.category IS 'Inferred category (e.g. waste management, water, sanitation)';
COMMENT ON COLUMN enriched_submissions.need_type IS 'Specific issue classification';
COMMENT ON COLUMN enriched_submissions.urgency IS 'Calculated urgency level (low, medium, high)';
COMMENT ON COLUMN enriched_submissions.sentiment IS 'Sentiment analysis of the citizen request';
COMMENT ON COLUMN enriched_submissions.canonical_location IS 'Geographic normalization mapping to recognized locations';
COMMENT ON COLUMN enriched_submissions.extracted_entities IS 'JSON metadata of extracted entities (places, agencies, etc.)';

-- Step 1: Create the 'embeddings' table to store vector embeddings for semantic search
CREATE TABLE IF NOT EXISTS embeddings (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    submission_id UUID UNIQUE NOT NULL REFERENCES submissions(id) ON DELETE CASCADE,
    vector vector(768) NOT NULL, -- Vertex AI text-embeddings defaults to 768 dimensions
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL
);

-- Add comments for 'embeddings' mapping to roadmap schema
COMMENT ON TABLE embeddings IS 'Stores high-dimensional vector embeddings generated on the English normalized text.';
COMMENT ON COLUMN embeddings.submission_id IS 'References the submission associated with this embedding';
COMMENT ON COLUMN embeddings.vector IS '768-dimensional float vector representation';

