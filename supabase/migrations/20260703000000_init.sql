-- Step 1: Create the 'priorities' table for final ranked recommendations
CREATE TABLE IF NOT EXISTS priorities (
    work_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    title TEXT NOT NULL,
    category TEXT NOT NULL,
    demand_score NUMERIC DEFAULT 0.0 NOT NULL,
    demand_count INTEGER DEFAULT 0 NOT NULL,
    hotspot_geo JSONB DEFAULT '{}'::jsonb NOT NULL,
    supporting_evidence JSONB DEFAULT '[]'::jsonb NOT NULL,
    rank INTEGER,
    explanation TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT now() NOT NULL
);

-- Add comments for 'priorities' mapping to roadmap schema
COMMENT ON TABLE priorities IS 'Stores ranked priority items synthesized by the data fusion and scoring algorithm.';
COMMENT ON COLUMN priorities.work_id IS 'Unique identifier for the priority work project';
COMMENT ON COLUMN priorities.title IS 'Short, descriptive title of the priority project';
COMMENT ON COLUMN priorities.category IS 'Primary category of the project';
COMMENT ON COLUMN priorities.demand_score IS 'Weighted rank score based on volume, urgency, and public data';
COMMENT ON COLUMN priorities.demand_count IS 'Raw count of citizen submissions supporting this priority';
