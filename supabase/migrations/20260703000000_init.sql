-- Step 3: Write the Postgres function (RPC) for similarity matching
CREATE OR REPLACE FUNCTION match_submissions (
    query_embedding vector(768),
    match_threshold float,
    match_count int
)
RETURNS TABLE (
    submission_id uuid,
    similarity float
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        embeddings.submission_id,
        (1 - (embeddings.vector <=> query_embedding))::float AS similarity
    FROM embeddings
    WHERE (1 - (embeddings.vector <=> query_embedding)) > match_threshold
    ORDER BY embeddings.vector <=> query_embedding ASC
    LIMIT match_count;
END;
$$;

COMMENT ON FUNCTION match_submissions IS 'Performs a pgvector top-k similarity search based on cosine distance.';
